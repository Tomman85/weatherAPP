class Weather {
  final bool isRainy;
  final int windSpeed;
  final int windDegree;
  final int cloudId;
  final int sunrise;
  final int sunset;
  final int realTemp;
  final int feelTemp;
  final int tempMin;
  final int tempMax;
  final int pressure;
  final int humidity;

  Weather(
      this.isRainy,
      this.windSpeed,
      this.windDegree,
      this.cloudId,
      this.sunrise,
      this.sunset,
      this.realTemp,
      this.feelTemp,
      this.tempMin,
      this.tempMax,
      this.pressure,
      this.humidity);
}
